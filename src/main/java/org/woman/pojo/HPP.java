package org.woman.pojo;

import java.util.List;

public class HPP {
	
	private String country;
	private String hppLink;
	private List<String> organizationList;

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getHppLink() {
		return hppLink;
	}

	public void setHppLink(String hppLink) {
		this.hppLink = hppLink;
	}

	public List<String> getOrganizationList() {
		return organizationList;
	}

	public void setOrganizationList(List<String> organizationList) {
		this.organizationList = organizationList;
	}

}
